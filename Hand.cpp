// 定数の定義 ==============================================================

//================================================================
// コンストラクタ
//================================================================
Hand::Hand()
{
	for (int i = 0; i < 11; i++)
	{
		m_card[i] = nullptr;
	}
	m_count = 0;
	m_rankCount = 0;
}

//================================================================
// デストラクタ
//================================================================
Hand::~Hand()
{
}

//================================================================
// ドローしたカードを手札へ追加(Hit)
//================================================================
void Hand::AddHand(Card* card)
{
	for (int i = 0; i < 11; i++)
	{
		if (m_card[i] == nullptr)
		{
			m_card[i] = card;
			m_rankCount += card->m_rank;
			m_count++;
			break;
		}
	}
}

//================================================================
// 手札の表示
//================================================================
void Hand::ShowHand()
{

	for (int i = 0; i < 11; i++)
	{
		if (m_card[i]!=nullptr)
		{
			m_card[i]->ShowCard();
		}
	}
	cout << endl;
	cout << "現在のカード枚数 : " << m_count << "枚" << endl;
}

//================================================================
// 手札の１枚目を表示（ディーラー専用）
//================================================================
void Hand::ShowDealerHand()
{
	if (m_card[0] != nullptr)
		m_card[0]->ShowCard();
	cout << endl;
	cout << "現在のカード枚数 : " << m_count << "枚" << endl;
}

