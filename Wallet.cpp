// ヘッダファイルの読み込み ================================================
#include <iostream>
#include "Wallet.h"
#include "Coin.h"

using namespace std;

// 定数の定義 ==============================================================

//================================================================
// コンストラクタ
//================================================================
Wallet::Wallet()
{
	m_count = 0;
}

//================================================================
// デストラクタ
//================================================================
Wallet::~Wallet()
{
}

//================================================================
// 受け取ったコインをウォレットへ追加
//================================================================
void Wallet::AddWallet(Coin* coin)
{
	m_coin.push_back(coin);
	m_count++;
}

//================================================================
// 所持コインの表示
//================================================================
void Wallet::ShowWallet()
{
	cout << "現在のコイン枚数 : " << m_count << "枚" << endl;
}

//================================================================
// ベットする
//================================================================
void Wallet::BetCoin(int bet)
{
	m_count -= bet;
	for (int i = 0; i < bet; i++)
	{
		m_coin.pop_back();
	}
}

